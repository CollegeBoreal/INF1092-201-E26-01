# --------------------------------------
# FUNCTION - Dynamic grouping
# --------------------------------------

function New-Groups {
    param (
        [array]$Items,
        [int]$Size
    )

    $groups = @()

    for ($i = 0; $i -lt $Items.Count; $i += $Size) {
        $end = [Math]::Min($i + $Size - 1, $Items.Count - 1)
        $groups += ,@($Items[$i..$end])
    }

    return $groups
}

